#include <libft.h>

void	ft_strdel(char **as)
{
	int		i;

	i = 0;
	if (as)
	{
		while ((*as)[i])
		{
			(*as)[i] = '\0';
			i++;
		}
		free(*as);
		*as = NULL;
	}
}
